<?php

namespace App\Entity;

use Doctrine\Common\Collections\ArrayCollection;
use Doctrine\Common\Collections\Collection;
use Doctrine\ORM\Mapping as ORM;

/**
 * @ORM\Entity(repositoryClass="App\Repository\SalleRepository")
 */
class Salle
{
    /**
     * @ORM\Id()
     * @ORM\GeneratedValue()
     * @ORM\Column(type="integer")
     */
    private $id;

    /**
     * @ORM\Column(type="string", length=50, nullable=true)
     */
    private $type_salle;

    /**
     * @ORM\Column(type="integer")
     */
    private $places;

    /**
     * @ORM\Column(type="integer")
     */
    private $prix;

    /**
     * @ORM\OneToMany(targetEntity="App\Entity\Location", mappedBy="salle")
     */
    private $locations;

    /**
     * @ORM\Column(type="integer", nullable=true)
     */
    private $caution;

    /**
     * @ORM\OneToOne(targetEntity="App\Entity\ProformatSalle", mappedBy="salle", cascade={"persist", "remove"})
     */
    private $proformatSalle;

    public function __construct()
    {
        $this->locations = new ArrayCollection();
    }

    public function getId(): ?int
    {
        return $this->id;
    }

    public function getTypeSalle(): ?string
    {
        return $this->type_salle;
    }

    public function setTypeSalle(?string $type_salle): self
    {
        $this->type_salle = $type_salle;

        return $this;
    }

    public function getPlaces(): ?int
    {
        return $this->places;
    }

    public function setPlaces(int $places): self
    {
        $this->places = $places;

        return $this;
    }

    public function getPrix(): ?int
    {
        return $this->prix;
    }

    public function setPrix(int $prix): self
    {
        $this->prix = $prix;

        return $this;
    }

    /**
     * @return Collection|Location[]
     */
    public function getLocations(): Collection
    {
        return $this->locations;
    }

    public function addLocation(Location $location): self
    {
        if (!$this->locations->contains($location)) {
            $this->locations[] = $location;
            $location->setSalle($this);
        }

        return $this;
    }

    public function removeLocation(Location $location): self
    {
        if ($this->locations->contains($location)) {
            $this->locations->removeElement($location);
            // set the owning side to null (unless already changed)
            if ($location->getSalle() === $this) {
                $location->setSalle(null);
            }
        }

        return $this;
    }

    public function getCaution(): ?int
    {
        return $this->caution;
    }

    public function setCaution(?int $caution): self
    {
        $this->caution = $caution;

        return $this;
    }

    public function getProformatSalle(): ?ProformatSalle
    {
        return $this->proformatSalle;
    }

    public function setProformatSalle(ProformatSalle $proformatSalle): self
    {
        $this->proformatSalle = $proformatSalle;

        // set the owning side of the relation if necessary
        if ($this !== $proformatSalle->getSalle()) {
            $proformatSalle->setSalle($this);
        }

        return $this;
    }
}
